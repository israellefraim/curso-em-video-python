frase = "Curso em Vídeo Python"
print(frase[0])  # Caractere do índice indicado
print(frase[0:13])  # Caracteres do índice 0 ao 12
print(frase[9:21])  # Caracteres do índice 9 ao 12
print(frase[0:21:2])  # Caracteres do índice 0 ao 21 pulando a cada 2 casas
print(frase[:5])  # Caracteres do índice 0 ao 5
print(frase[15:])  # Início no índice 15 até o final da variável
print(frase[9::3])  # Início no índice 9 até o final da variável pulando a cada 3 casas
print(frase[::4])  # Início no índice 0 até o final da variável pulado a cada 4 casas
